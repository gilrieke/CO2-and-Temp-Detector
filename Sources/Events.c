/*
** ===================================================================
**     Event       :  TI1_OnInterrupt (module Events)
**
**     Component   :  TI1 [TimerInt]
**     Description :
**         When a timer interrupt occurs this event is called (only
**         when the component is enabled - <Enable> and the events are
**         enabled - <EnableEvent>). This event is enabled only if a
**         <interrupt service/event> is enabled.
**     Parameters  : None
**     Returns     : Nothing
** ===================================================================
*/
void TI1_OnInterrupt(LDD_TUserData *UserDataPtr)
{
	
	// Enviamos datos
	
	if(state) {														// Esta activo el sistema ???
		
		if(contador == 10) {
			// preguntar por estado 
			TI1_Disable(TI1_DeviceData); 				// desactivamos el temporizador 
			contador = 0;									
			
			// Leemos Temperatura 
			AD1_Measure(TRUE);								// Iniciamos medicion
			AD1_GetValue8(&temperatura);				
			temperatura = temperatura*0.588;		// Convertimos a Temperatura
			
			// Leemos CO2
			co2 = read_CO2();
			
			select_state();
			fan_speed_SetDutyMS(10 - fan_speed);
			servo_degree(window_position);
			
			
			Term1_SendStr("data");							// Enviamos cadena de datos
			Term1_CRLF();
			Term1_SendNum(state_interior);
			Term1_CRLF();
			Term1_SendNum(temperatura);
			Term1_CRLF();
			Term1_SendNum(co2);
			Term1_CRLF();
			Term1_SendNum(fan_speed);
			Term1_CRLF();
			Term1_SendNum(window_position);
			Term1_CRLF();
			
			TI1_Enable(TI1_DeviceData);
		}
	}

	contador++;
}

